package com.gs.lshly.common.enums;

/**
 * @Author Starry
 * @Date 16:07 2020/10/16
 * 是否匿名咨询
 */
public enum RegionLevelEnum implements EnumMessage{

    省(1,"省"),
    市(2,"市"),
    县区(3,"县区");

    private Integer code;
    private String remark;

    RegionLevelEnum(Integer code, String remark) {
        this.code = code;
        this.remark = remark;
    }

    @Override
    public Integer getCode() {
        return code;
    }

    @Override
    public String getRemark() {
        return remark;
    }
}
